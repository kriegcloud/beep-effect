import { passkeysAtom } from "@beep/iam-sdk/clients/passkey";
import { useAtomRefresh } from "@effect-atom/atom-react";
import Alert from "@mui/material/Alert";
import Box from "@mui/material/Box";
import Button from "@mui/material/Button";

export const PasskeysFallback: React.FC = () => {
  const refresh = useAtomRefresh(passkeysAtom.remote);
  return (
    <Box>
      <Alert severity={"error"} action={<Button onClick={refresh}>Retry</Button>}>
        Something went wrong...
      </Alert>
    </Box>
  );
};
