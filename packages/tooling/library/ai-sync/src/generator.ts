/**
 * Generate committed schema and source metadata files.
 *
 * @example
 * ```ts
 * import * as NodeServices from "@effect/platform-node/NodeServices"
 * import { Effect, Layer } from "effect"
 * import { AiSyncHttpLayer, generateAiSyncArtifacts } from "@beep/ai-sync/generator"
 *
 * const RuntimeLayer = Layer.mergeAll(NodeServices.layer, AiSyncHttpLayer)
 * const program = generateAiSyncArtifacts().pipe(
 *   Effect.as("generated"),
 *   Effect.provide(RuntimeLayer)
 * )
 *
 * Effect.runPromise(program).then(console.log)
 * ```
 * @effects Fetches Tier-1 source bodies, computes content hashes, writes the
 * committed generated schema and source-metadata files, and logs a completion
 * message.
 * @category workflows
 * @since 0.0.0
 */
export const generateAiSyncArtifacts = Effect.fn("AiSync.generateAiSyncArtifacts")(function* () {
  const sources = yield* withSourceHashes;
  yield* writePackageFile(GENERATED_SCHEMAS_PATH, renderGeneratedSchemas());
  yield* writePackageFile(GENERATED_SOURCE_METADATA_PATH, renderGeneratedSourceMetadata(sources));
  yield* Console.log(`Generated ${A.length(sources)} AI sync source pins.`);
});

