/**
 * Compute a public SHA-256 content hash.
 *
 * @example
 * ```ts
 * import { Effect } from "effect"
 * import { hashSourceText } from "@beep/ai-sync/generator"
 *
 * Effect.runPromise(hashSourceText("agent instructions")).then((hash) =>
 *   console.log(hash.length)
 * )
 * ```
 * @effects Uses the Web Crypto SHA-256 implementation when the returned Effect
 * is executed; requires no Effect services.
 * @category utilities
 * @since 0.0.0
 */
export const hashSourceText: (value: string) => Effect.Effect<string, AiSyncError> = Effect.fn("AiSync.hashSourceText")(
  function* (value) {
    return yield* Effect.tryPromise({
      try: () => globalThis.crypto.subtle.digest("SHA-256", new TextEncoder().encode(value)),
      catch: (cause) =>
        AiSyncError.make({
          message: "Failed to compute source content hash.",
          cause,
        }),
    }).pipe(Effect.map((buffer) => Encoding.encodeHex(new Uint8Array(buffer))));
  }
);

/**
 * Fetch an upstream source body.
 *
 * @example
 * ```ts
 * import { Effect } from "effect"
 * import { AiSyncSourceMetadata } from "@beep/ai-sync"
 * import { AiSyncHttpLayer, fetchSourceText } from "@beep/ai-sync/generator"
 *
 * const source = AiSyncSourceMetadata.make({
 *   id: "codex-config",
 *   agent: "codex",
 *   domain: "config",
 *   tier: "tier_1",
 *   url: "https://example.com/config.schema.json",
 *   isOfficial: true,
 *   driftMechanism: "hash"
 * })
 * const program = fetchSourceText(source).pipe(
 *   Effect.map((text) => text.length),
 *   Effect.provide(AiSyncHttpLayer)
 * )
 *
 * Effect.runPromise(program).then(console.log)
 * ```
 * @effects Performs one HTTP GET through the active Effect HTTP client and
 * fails with {@link AiSyncError} when the response cannot be fetched as text.
 * @category services
 * @since 0.0.0
 */
export const fetchSourceText = Effect.fn("AiSync.fetchSourceText")(function* (source: AiSyncSourceMetadata) {
  return yield* HttpClient.get(source.url).pipe(
    Effect.flatMap(HttpClientResponse.filterStatusOk),
    Effect.flatMap((response) => response.text),
    Effect.mapError((cause) =>
      AiSyncError.make({
        message: `Failed to fetch upstream source ${source.id}.`,
        sourceId: source.id,
        cause,
      })
    )
  );
});

const withSourceHashes = Effect.forEach(
  sortedSources(),
  (source) =>
    fetchSourceText(source).pipe(
      Effect.flatMap(hashSourceText),
      Effect.map((contentHash) =>
        AiSyncSourceMetadata.make({
          id: source.id,
          agent: source.agent,
          domain: source.domain,
          tier: source.tier,
          url: source.url,
          ...O.getSomesStruct({ versionPin: O.fromUndefinedOr(source.versionPin) }),
          contentHash,
          isOfficial: source.isOfficial,
          driftMechanism: source.driftMechanism,
        })
      )
    ),
  { concurrency: 3 }
);

const packageRoot = Effect.fn("AiSync.packageRoot")(function* () {
  const path = yield* Path.Path;
  return path.resolve(import.meta.dirname, "..");
});

const writePackageFile = Effect.fn("AiSync.writePackageFile")(function* (relativePath: string, content: string) {
  const fs = yield* FileSystem.FileSystem;
  const path = yield* Path.Path;
  const root = yield* packageRoot();
  const outputPath = path.join(root, relativePath);
  yield* fs.makeDirectory(path.dirname(outputPath), { recursive: true });
  yield* fs.writeFileString(outputPath, content);
});

