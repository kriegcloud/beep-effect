const sourceOrder = Order.mapInput(Order.String, (source: AiSyncSourceMetadata) => source.id);

const sortedSources = () => A.sort(TIER_ONE_SOURCES, sourceOrder);

const SJsonString: (value: string) => string = JSON.stringify;

const quote = (value: string) => SJsonString(value);

const renderOptionalStringField = (name: string, value: string | undefined): ReadonlyArray<string> =>
  value === undefined ? [] : [`    ${name}: ${quote(value)},`];

const renderOptionalLiteralKitField = (name: string, literals: ReadonlyArray<string>): ReadonlyArray<string> => [
  `    ${name}: LiteralKit([`,
  ...pipe(
    literals,
    A.map((literal) => `      ${quote(literal)},`)
  ),
  "    ]).pipe(S.optionalKey),",
];

const renderSourceMetadata = (source: AiSyncSourceMetadata): string =>
  pipe(
    [
      "  {",
      `    id: ${quote(source.id)},`,
      `    agent: ${quote(source.agent)},`,
      `    domain: ${quote(source.domain)},`,
      `    tier: ${quote(source.tier)},`,
      `    url: ${quote(source.url)},`,
      ...renderOptionalStringField("versionPin", source.versionPin),
      ...renderOptionalStringField("contentHash", source.contentHash),
      `    isOfficial: ${source.isOfficial ? "true" : "false"},`,
      `    driftMechanism: ${quote(source.driftMechanism)},`,
      "  },",
    ],
    A.join("\n")
  );

